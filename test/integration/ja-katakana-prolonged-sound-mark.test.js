// textlint-tester v15 は CJS (exports.default) のため ESM 側で2段階 unwrap が必要
import _textlintTesterModule from "textlint-tester";
const TextlintTester = _textlintTesterModule.default;

import rule from "../../lib/ja-katakana-prolonged-sound-mark.js";

const tester = new TextlintTester();

// =============================================================================
// 1. 基本動作
//    規則・慣例それぞれの代表語で、メッセージ文言と fix を検証する。
// =============================================================================
tester.run("基本動作", rule, {
  valid: [
    { text: "ユーザー" }, // 規則: 長音符付加方向の正表記
    { text: "メモリ" },   // 規則: 長音符削除方向の正表記
    { text: "クリア" },   // 慣例: 長音符なしを正とする
    { text: "サマリー" }, // 慣例: 長音符ありを正とする
  ],
  invalid: [
    {
      text: "ユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザー",
    },
    {
      text: "メモリー",
      errors: [{ message: "「メモリー」は「メモリ」と表記してください。" }],
      output: "メモリ",
    },
    {
      text: "クリアー",
      errors: [{ message: "「クリアー」は「クリア」と表記してください。" }],
      output: "クリア",
    },
    {
      text: "サマリ",
      errors: [{ message: "「サマリ」は「サマリー」と表記してください。" }],
      output: "サマリー",
    },
  ],
});

// =============================================================================
// 2. 辞書非収録語のスキップ
//    辞書に収録されていない語はすべてスキップされる（自動判定なし）。
// =============================================================================
tester.run("辞書非収録語のスキップ", rule, {
  valid: [
    { text: "データ" },           // data (-a): 辞書非収録
    { text: "テーブル" },         // table (-le): 辞書非収録
    { text: "ウィンドウ" },       // window (-ow): 辞書非収録
    { text: "インターフェイス" }, // interface: 辞書非収録
    { text: "コンソール" },       // console (-le): 辞書非収録
  ],
  invalid: [],
});

// =============================================================================
// 3. allow-both の許容
//    両方許容に分類した語は、長音符あり・なしのどちらでもエラーにしない。
// =============================================================================
tester.run("allow-both の許容", rule, {
  valid: [
    { text: "スカラ" },
    { text: "スカラー" },
  ],
  invalid: [],
});

// =============================================================================
// 4. 区切り文字境界
//    非カタカナ文字はすべて自然な区切りになる。
//    U+30FB（・）は U+30A0-U+30FF ブロック内だが正規表現から除外されている。
// =============================================================================
tester.run("区切り文字境界: 正表記はエラーなし", rule, {
  valid: [
    { text: "ユーザーが操作する" },   // 文頭
    { text: "設定対象はユーザー" },   // 文末
    { text: "現在のユーザーを確認" }, // 文中
    { text: "ユーザー・フォルダー" }, // 中黒（U+30FB）
    { text: "ユーザー、フォルダー" }, // 読点
    { text: "ユーザー。" },           // 句点
    { text: "ユーザー／フォルダー" }, // 全角スラッシュ
    { text: "ユーザー/フォルダー" },  // 半角スラッシュ
    { text: "（ユーザー）" },         // 全角丸括弧
    { text: "(ユーザー)" },           // 半角丸括弧
    { text: "「ユーザー」" },         // 鉤括弧
    { text: "ユーザー フォルダー" },  // 半角スペース
    { text: "ユーザー　フォルダー" }, // 全角スペース
    { text: "ユーザー…フォルダー" }, // 三点リーダー
  ],
  invalid: [],
});

tester.run("区切り文字境界: 誤表記が区切りを越えず独立して検知される", rule, {
  valid: [],
  invalid: [
    // 文頭
    {
      text: "ユーザが操作する",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザーが操作する",
    },
    // 文末
    {
      text: "設定対象はユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "設定対象はユーザー",
    },
    // 文中
    {
      text: "現在のユーザを確認",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "現在のユーザーを確認",
    },
    // 中黒（U+30FB）
    {
      text: "ユーザ・フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー・フォルダー",
    },
    // 読点
    {
      text: "ユーザ、フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー、フォルダー",
    },
    // 句点
    {
      text: "ユーザ。",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザー。",
    },
    // 全角スラッシュ
    {
      text: "ユーザ／フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー／フォルダー",
    },
    // 半角スラッシュ
    {
      text: "ユーザ/フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー/フォルダー",
    },
    // 全角丸括弧
    {
      text: "（ユーザ）",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "（ユーザー）",
    },
    // 半角丸括弧
    {
      text: "(ユーザ)",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "(ユーザー)",
    },
    // 鉤括弧
    {
      text: "「ユーザ」",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "「ユーザー」",
    },
    // 半角スペース
    {
      text: "ユーザ フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー フォルダー",
    },
    // 全角スペース
    {
      text: "ユーザ　フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー　フォルダー",
    },
    // 三点リーダー
    {
      text: "ユーザ…フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー…フォルダー",
    },
  ],
});

// =============================================================================
// 5. 複合語の非検知
//    辞書は完全一致のため、辞書エントリをカタカナ連続部分として含む語はスキップ。
// =============================================================================
tester.run("複合語の非検知", rule, {
  valid: [
    { text: "サーバプール" },       // 「サーバ」を含むが複合語全体は辞書非収録
    { text: "フォルダツリー" },     // 「フォルダ」を含むが複合語全体は辞書非収録
    { text: "ユーザリスト" },       // 「ユーザ」を含むが複合語全体は辞書非収録
    { text: "エディタプラグイン" }, // 「エディタ」を含むが複合語全体は辞書非収録
    { text: "サーバープール" },     // 正表記成分を含む複合語
    { text: "ユーザーリスト" },     // 正表記成分を含む複合語
  ],
  invalid: [],
});

// =============================================================================
// 6. 複数エラーの独立性
//    複数エラーが同一テキストに存在する場合、各 fix が独立して正しく適用される。
// =============================================================================
tester.run("複数エラーの独立性", rule, {
  valid: [
    { text: "ユーザーとフォルダー" },
  ],
  invalid: [
    {
      text: "ユーザとユーザ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
      ],
      output: "ユーザーとユーザー",
    },
    {
      text: "ユーザーとユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザーとユーザー",
    },
    {
      text: "ユーザとフォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザーとフォルダー",
    },
    {
      text: "ユーザとメモリー",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「メモリー」は「メモリ」と表記してください。" },
      ],
      output: "ユーザーとメモリ",
    },
  ],
});

// =============================================================================
// 7. 明示登録された派生語・偽同定防止語の完全一致検出
//    derived / falsePositives として登録された語は、完全一致で検出される。
//    エラーメッセージ・fix の range は語全体を参照する。
// =============================================================================
tester.run("明示登録語の完全一致検出", rule, {
  valid: [
    // 正表記
    { text: "アンインストーラー" }, // derived: installer 派生
    { text: "デシリアライザ" },     // derived: serializer 派生
    { text: "ディスクロージャ" },   // derived: closure 派生
    { text: "ブリーダー" },         // falsePositive: breeder（reader と誤同定されないため登録）
    { text: "プランナー" },         // falsePositive: planner（runner と誤同定されないため登録）
    { text: "ニュークリア" },       // falsePositive: nuclear（clear と誤同定されないため登録）
  ],
  invalid: [
    // 派生語の誤表記検出（メッセージが語全体を参照）
    {
      text: "アンインストーラ",
      errors: [{ message: "「アンインストーラ」は「アンインストーラー」と表記してください。" }],
      output: "アンインストーラー",
    },
    {
      text: "デシリアライザー",
      errors: [{ message: "「デシリアライザー」は「デシリアライザ」と表記してください。" }],
      output: "デシリアライザ",
    },
    {
      text: "インフラストラクチャー",
      errors: [{ message: "「インフラストラクチャー」は「インフラストラクチャ」と表記してください。" }],
      output: "インフラストラクチャ",
    },
    // 偽同定防止語の誤表記検出
    {
      text: "ブリーダ",
      errors: [{ message: "「ブリーダ」は「ブリーダー」と表記してください。" }],
      output: "ブリーダー",
    },
    {
      text: "プランナ",
      errors: [{ message: "「プランナ」は「プランナー」と表記してください。" }],
      output: "プランナー",
    },
    {
      text: "ニュークリアー",
      errors: [{ message: "「ニュークリアー」は「ニュークリア」と表記してください。" }],
      output: "ニュークリア",
    },
  ],
});
