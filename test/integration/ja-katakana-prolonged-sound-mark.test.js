// textlint-tester v15 は CJS (exports.default) のため ESM 側で2段階 unwrap が必要
import _textlintTesterModule from "textlint-tester";
const TextlintTester = _textlintTesterModule.default;

import rule from "../../lib/ja-katakana-prolonged-sound-mark.js";

const tester = new TextlintTester();

// =============================================================================
// 1. 基本動作
//    規則・慣例それぞれの代表語で、メッセージ文言と fix を検証する。
// =============================================================================
tester.run("基本動作", rule, {
  valid: [
    { text: "ユーザー" }, // 規則: 長音符付加方向の正表記
    { text: "メモリ" }, // 規則: 長音符削除方向の正表記
    { text: "クリア" }, // 慣例: 長音符なしを正とする
    { text: "サマリー" }, // 慣例: 長音符ありを正とする
  ],
  invalid: [
    {
      text: "ユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザー",
    },
    {
      text: "メモリー",
      errors: [{ message: "「メモリー」は「メモリ」と表記してください。" }],
      output: "メモリ",
    },
    {
      text: "クリアー",
      errors: [{ message: "「クリアー」は「クリア」と表記してください。" }],
      output: "クリア",
    },
    {
      text: "サマリ",
      errors: [{ message: "「サマリ」は「サマリー」と表記してください。" }],
      output: "サマリー",
    },
  ],
});

// =============================================================================
// 2. 辞書非収録語のスキップ
//    辞書に収録されていない語はすべてスキップされる（自動判定なし）。
// =============================================================================
tester.run("辞書非収録語のスキップ", rule, {
  valid: [
    { text: "データ" }, // data (-a): 辞書非収録
    { text: "テーブル" }, // table (-le): 辞書非収録
    { text: "ウィンドウ" }, // window (-ow): 辞書非収録
    { text: "インターフェイス" }, // interface: 辞書非収録
    { text: "コンソール" }, // console (-le): 辞書非収録
  ],
  invalid: [],
});

// =============================================================================
// 3. allow-both の許容
//    両方許容に分類した語は、長音符あり・なしのどちらでもエラーにしない。
// =============================================================================
tester.run("allow-both の許容", rule, {
  valid: [{ text: "スカラ" }, { text: "スカラー" }],
  invalid: [],
});

// =============================================================================
// 4. 区切り文字境界
//    非カタカナ文字はすべて自然な区切りになる。
//    U+30FB（・）は U+30A0-U+30FF ブロック内だが正規表現から除外されている。
// =============================================================================
tester.run("区切り文字境界: 正表記はエラーなし", rule, {
  valid: [
    { text: "ユーザーが操作する" }, // 文頭
    { text: "設定対象はユーザー" }, // 文末
    { text: "現在のユーザーを確認" }, // 文中
    { text: "ユーザー・フォルダー" }, // 中黒（U+30FB）
    { text: "ユーザー、フォルダー" }, // 読点
    { text: "ユーザー。" }, // 句点
    { text: "ユーザー／フォルダー" }, // 全角スラッシュ
    { text: "ユーザー/フォルダー" }, // 半角スラッシュ
    { text: "（ユーザー）" }, // 全角丸括弧
    { text: "(ユーザー)" }, // 半角丸括弧
    { text: "「ユーザー」" }, // 鉤括弧
    { text: "ユーザー フォルダー" }, // 半角スペース
    { text: "ユーザー　フォルダー" }, // 全角スペース
    { text: "ユーザー…フォルダー" }, // 三点リーダー
  ],
  invalid: [],
});

tester.run("区切り文字境界: 誤表記が区切りを越えず独立して検知される", rule, {
  valid: [],
  invalid: [
    // 文頭
    {
      text: "ユーザが操作する",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザーが操作する",
    },
    // 文末
    {
      text: "設定対象はユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "設定対象はユーザー",
    },
    // 文中
    {
      text: "現在のユーザを確認",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "現在のユーザーを確認",
    },
    // 中黒（U+30FB）
    {
      text: "ユーザ・フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー・フォルダー",
    },
    // 読点
    {
      text: "ユーザ、フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー、フォルダー",
    },
    // 句点
    {
      text: "ユーザ。",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザー。",
    },
    // 全角スラッシュ
    {
      text: "ユーザ／フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー／フォルダー",
    },
    // 半角スラッシュ
    {
      text: "ユーザ/フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー/フォルダー",
    },
    // 全角丸括弧
    {
      text: "（ユーザ）",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "（ユーザー）",
    },
    // 半角丸括弧
    {
      text: "(ユーザ)",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "(ユーザー)",
    },
    // 鉤括弧
    {
      text: "「ユーザ」",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "「ユーザー」",
    },
    // 半角スペース
    {
      text: "ユーザ フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー フォルダー",
    },
    // 全角スペース
    {
      text: "ユーザ　フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー　フォルダー",
    },
    // 三点リーダー
    {
      text: "ユーザ…フォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザー…フォルダー",
    },
  ],
});

// =============================================================================
// 5. 後方一致で検出されない複合語
//    wrongForm が語の先頭・中間にある複合語は検出されない（後方一致は末尾のみ対象）。
//    末尾が辞書非収録語で終わる複合語も同様に検出されない。
// =============================================================================
tester.run("後方一致で検出されない複合語", rule, {
  valid: [
    { text: "サーバプール" }, // 「サーバ」は先頭、「プール」は辞書非収録
    { text: "フォルダツリー" }, // 「フォルダ」は先頭、「ツリー」は辞書非収録
    { text: "ユーザリスト" }, // 「ユーザ」は先頭、「リスト」は辞書非収録
    { text: "エディタプラグイン" }, // 「エディタ」は先頭、「プラグイン」は辞書非収録
    { text: "サーバープール" }, // 正表記成分を含む複合語
    { text: "ユーザーリスト" }, // 正表記成分を含む複合語
  ],
  invalid: [],
});

// =============================================================================
// 6. 複数エラーの独立性
//    複数エラーが同一テキストに存在する場合、各 fix が独立して正しく適用される。
// =============================================================================
tester.run("複数エラーの独立性", rule, {
  valid: [{ text: "ユーザーとフォルダー" }],
  invalid: [
    {
      text: "ユーザとユーザ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
      ],
      output: "ユーザーとユーザー",
    },
    {
      text: "ユーザーとユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザーとユーザー",
    },
    {
      text: "ユーザとフォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザーとフォルダー",
    },
    {
      text: "ユーザとメモリー",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「メモリー」は「メモリ」と表記してください。" },
      ],
      output: "ユーザーとメモリ",
    },
  ],
});

// =============================================================================
// 7. 明示登録された variants と独立エントリの完全一致検出
//    variants と独立エントリに昇格した語は、完全一致で検出される。
//    エラーメッセージ・fix の range は語全体を参照する。
// =============================================================================
tester.run("明示登録語の完全一致検出", rule, {
  valid: [
    // 正表記
    { text: "アンインストーラー" }, // variants: installer 派生
    { text: "デシリアライザ" }, // variants: serializer 派生
    { text: "ディスクロージャ" }, // variants: closure 派生
    { text: "ハイライター" }, // variants: lighter 派生
    { text: "プランナー" }, // 独立 requireMark
  ],
  invalid: [
    // 派生語の誤表記検出（メッセージが語全体を参照）
    {
      text: "アンインストーラ",
      errors: [{ message: "「アンインストーラ」は「アンインストーラー」と表記してください。" }],
      output: "アンインストーラー",
    },
    {
      text: "デシリアライザー",
      errors: [{ message: "「デシリアライザー」は「デシリアライザ」と表記してください。" }],
      output: "デシリアライザ",
    },
    {
      text: "インフラストラクチャー",
      errors: [
        { message: "「インフラストラクチャー」は「インフラストラクチャ」と表記してください。" },
      ],
      output: "インフラストラクチャ",
    },
    {
      text: "ハイライタ",
      errors: [{ message: "「ハイライタ」は「ハイライター」と表記してください。" }],
      output: "ハイライター",
    },
    {
      text: "プランナ",
      errors: [{ message: "「プランナ」は「プランナー」と表記してください。" }],
      output: "プランナー",
    },
  ],
});

// =============================================================================
// 8. allowBoth 登録語は検知されない
//    falsePositives から allowBoth へ移した語は、完全一致でも後方一致でも保護される。
// =============================================================================
tester.run("allowBoth 登録語は検知されない", rule, {
  valid: [
    { text: "ブリーダ" },
    { text: "ブリーダー" },
    { text: "Xブリーダ" },
    { text: "Xブリーダー" },
    { text: "プロテスタ" },
    { text: "プロテスター" },
    { text: "ニュークリア" },
    { text: "ニュークリアー" },
  ],
  invalid: [],
});

// =============================================================================
// 9. cross-ref から独立エントリへ昇格した語の完全一致検出
//    偽同定防止だけでなく、独立した語として検知対象になる。
// =============================================================================
tester.run("cross-ref 昇格語の完全一致検出", rule, {
  valid: [
    { text: "レポーター" },
    { text: "コンカレンシー" },
    { text: "サポータビリティ" },
    { text: "ディスパッチャー" },
  ],
  invalid: [
    {
      text: "レポータ",
      errors: [{ message: "「レポータ」は「レポーター」と表記してください。" }],
      output: "レポーター",
    },
    {
      text: "サポータ",
      errors: [{ message: "「サポータ」は「サポーター」と表記してください。" }],
      output: "サポーター",
    },
    {
      text: "コンカレンシ",
      errors: [{ message: "「コンカレンシ」は「コンカレンシー」と表記してください。" }],
      output: "コンカレンシー",
    },
    {
      text: "サポータビリティー",
      errors: [{ message: "「サポータビリティー」は「サポータビリティ」と表記してください。" }],
      output: "サポータビリティ",
    },
    {
      text: "ディスパッチャ",
      errors: [{ message: "「ディスパッチャ」は「ディスパッチャー」と表記してください。" }],
      output: "ディスパッチャー",
    },
  ],
});

// =============================================================================
// 10. 後方一致による複合語の検出
//    wrongForm が語の末尾に一致する複合語は後方一致フォールバックで検出される。
//    エラーメッセージは一致した末尾部分のみを参照し、fix も末尾だけを差し替える。
// =============================================================================
tester.run("後方一致による複合語の検出", rule, {
  valid: [
    // 末尾が正表記の複合語は検出されない
    { text: "ウェブサーバー" },
    { text: "ファイルマネージャー" },
    { text: "レイヤードアーキテクチャ" },
  ],
  invalid: [
    {
      text: "ウェブサーバ",
      errors: [{ message: "「サーバ」は「サーバー」と表記してください。" }],
      output: "ウェブサーバー",
    },
    {
      text: "ファイルマネージャ",
      errors: [{ message: "「マネージャ」は「マネージャー」と表記してください。" }],
      output: "ファイルマネージャー",
    },
    {
      text: "レイヤードアーキテクチャー",
      errors: [{ message: "「アーキテクチャー」は「アーキテクチャ」と表記してください。" }],
      output: "レイヤードアーキテクチャ",
    },
    // 文中での後方一致: fix が末尾だけを差し替える
    {
      text: "外部のウェブサーバに接続する",
      errors: [{ message: "「サーバ」は「サーバー」と表記してください。" }],
      output: "外部のウェブサーバーに接続する",
    },
  ],
});

// =============================================================================
// 11. 完全一致の優先
//    wrongForm が完全一致する場合、後方一致は走らず、メッセージは語全体を参照する。
// =============================================================================
tester.run("完全一致の優先", rule, {
  valid: [],
  invalid: [
    // アンインストーラ は variants として完全一致するため、後方一致の インストーラ ではなく
    // 語全体 アンインストーラ がメッセージに現れる
    {
      text: "アンインストーラ",
      errors: [{ message: "「アンインストーラ」は「アンインストーラー」と表記してください。" }],
      output: "アンインストーラー",
    },
    // ディスパッチャ も同様（独立エントリとして完全一致、後方一致の パッチャ ではない）
    {
      text: "ディスパッチャ",
      errors: [{ message: "「ディスパッチャ」は「ディスパッチャー」と表記してください。" }],
      output: "ディスパッチャー",
    },
  ],
});

// =============================================================================
// 12. 最長一致
//     複数の wrongForm が語の末尾に一致する場合、最長のものが採用される。
// =============================================================================
tester.run("最長一致", rule, {
  valid: [],
  invalid: [
    // サブダウンローダ は ダウンローダ(6字) と ローダ(3字) の両方に末尾一致するが、
    // 最長一致により ダウンローダ が選ばれる
    {
      text: "サブダウンローダ",
      errors: [{ message: "「ダウンローダ」は「ダウンローダー」と表記してください。" }],
      output: "サブダウンローダー",
    },
  ],
});
