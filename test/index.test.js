// =============================================================================
// 6. 複数エラーと fix の独立性
//    複数エラーが同一テキストに存在する場合、各 fix が独立して正しく適用される。
// =============================================================================
tester.run("複数エラーと fix の独立性", rule, {
  valid: [
    { text: "ユーザーとフォルダー" }, // 2語とも正表記
  ],
  invalid: [
    // 同一語が2回（両方誤表記）
    {
      text: "ユーザとユーザ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
      ],
      output: "ユーザーとユーザー",
    },
    // 同一語が2回（正誤混在: 正表記は無視、誤表記のみ検知）
    {
      text: "ユーザーとユーザ",
      errors: [{ message: "「ユーザ」は「ユーザー」と表記してください。" }],
      output: "ユーザーとユーザー",
    },
    // 異なる2語（両方誤表記）
    {
      text: "ユーザとフォルダ",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「フォルダ」は「フォルダー」と表記してください。" },
      ],
      output: "ユーザーとフォルダー",
    },
    // 異なる2語（正誤混在: 誤表記のみ検知）
    {
      text: "ユーザーとフォルダ",
      errors: [{ message: "「フォルダ」は「フォルダー」と表記してください。" }],
      output: "ユーザーとフォルダー",
    },
    // 末尾ー付加と末尾ー削除が混在
    {
      text: "ユーザとメモリー",
      errors: [
        { message: "「ユーザ」は「ユーザー」と表記してください。" },
        { message: "「メモリー」は「メモリ」と表記してください。" },
      ],
      output: "ユーザーとメモリ",
    },
  ],
});

// =============================================================================
// 7. カタカナ連続語との境界（部分一致の非検知）
//    辞書は完全一致のため、辞書エントリをカタカナ連続部分として含む語はスキップされる。
//    これは辞書方式の設計上の仕様。複合語の誤り成分は検知対象外。
// =============================================================================
tester.run("カタカナ連続語との境界: 複合語は辞書非収録としてスキップ", rule, {
  valid: [
    // 誤表記成分を含む複合語 → 全体が辞書にないためスキップ（設計上の仕様）
    { text: "サーバプール" },       // 「サーバ」を含むが複合語全体は辞書非収録
    { text: "フォルダツリー" },     // 「フォルダ」を含むが複合語全体は辞書非収録
    { text: "ユーザリスト" },       // 「ユーザ」を含むが複合語全体は辞書非収録
    { text: "エディタプラグイン" }, // 「エディタ」を含むが複合語全体は辞書非収録
    // 正表記成分を含む複合語 → エラーなし
    { text: "サーバープール" },     // 「サーバー」(正表記) を含む複合語
    { text: "ユーザーリスト" },     // 「ユーザー」(正表記) を含む複合語
  ],
  invalid: [],
});
